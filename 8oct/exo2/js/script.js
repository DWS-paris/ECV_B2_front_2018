/*
Déclare une variable
- Mot clef : "let" (équivalent ES5 "var")
- Ajouter de la valeur : "="
- Algo : firstName <- "Julien"
*/
    let firstName = 'Julien';

    // Afficher la variable dans la console
    console.log( firstName );
//

/*
Les types de variable primitive
- Chaîne de caractères : "string" (tableau de lettres)
- Un nombre entier : "integer"
- Un nombre flottant : "float"
- Une valeur vraie ou fausse : "boolean"
*/
    let myString = 'Chaîne de caractères';
    let myStringInteger = '39';
    let myInteger = 39;
    let myFloat = .23; //=> 0.23
    let myBoolean = true; //=> true/false

    // Afficher les variables dans le console
    console.log( myString, myStringInteger, myInteger, myFloat, myBoolean );
//